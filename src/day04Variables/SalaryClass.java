package day04Variables;

public class SalaryClass {
    //hourly rate, weekly hours

    public static void main(String[] args) {

        int hourslyRate=65;
        int weeklyHours=45;
        int numberOfWeeks=52;

        int sallary=hourslyRate*weeklyHours*numberOfWeeks;
        System.out.println("salary $"+sallary);
        System.out.println("sallary = $" + sallary);
        System.out.println("weeklyHours = " + weeklyHours);
        System.out.println("hourslyRate = $" + hourslyRate);
        System.out.println("numberOfWeeks = " + numberOfWeeks);

    }



}
