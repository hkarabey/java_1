package day06Casting;

public class AritmeticOperators {

    public static void main(String[] args) {

        System.out.println(10+20);//System.out.println("10"+20);=1020
                                    // String+data farklıdır.
        System.out.println(20-10);
        System.out.println(10*5);
        System.out.println(10/3);
        System.out.println(10.0/3);
        System.out.println((double)10/3);
        System.out.println(10/3.0);
        System.out.println(10d/3);
        System.out.println(11.0*2);
        System.out.println(12.2-11);
        int a=100;
        double b=a/6;
        System.out.println(b);
    }

    /*
    + aditional
    - subtraction
    * multıpılatıon
    / division
    % remınder
     */

}
