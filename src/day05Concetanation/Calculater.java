package day05Concetanation;

public class Calculater {
    public static void main(String[] args) {

        int firsName = 100;
        int secondName = 50;
        //100+50=150
        //100-50=50
        //100*50=5000;
        int add = firsName + secondName;
        int substraction = firsName - secondName;
        int plus = firsName * secondName;
        System.out.println(firsName + "+" + secondName + "=" + add);
        System.out.println(firsName + "-" + secondName + "=" + substraction);
        System.out.println(firsName + "*" + secondName + "=" + plus);
    }


}
